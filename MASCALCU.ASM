 MOV RESU, AL
 MOV AH, 00
 AAD

 ADD AH, 30H
 ADD AL, 30H

 MOV BX, AX
 LEA DX, RESULT
 MOV AH, 9
 INT 21H

 MOV AH, 2
 MOV DL, BH
 INT 21H

 MOV AH, 2
 MOV DL, BL
 INT 21H
 MOV AH, 4CH
 INT 21H

