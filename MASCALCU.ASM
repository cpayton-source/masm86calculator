 MOV AH, 0                  
 AAA

;CONVERT TO NUM AND READY FOR DISPLAY
 MOV BX, AX
 ADD BH, 48
 ADD BL, 48

;DISPLAY ANSWER
 LEA DX, RESULT
 MOV AH, 9
 INT 21H

;GET FIRST PART OF ANSWER INTO DL REG
 MOV AH, 2
 MOV DL, BH
 INT 21H

;GET SECOND PART OF ANSWER INTO DL REG
 MOV AH, 2
 MOV DL, BL
 INT 21H

