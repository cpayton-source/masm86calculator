 INT 21H
 MOV BL, AL

;PROMPT FOR SECOND NUM
 LEA DX, PROMPT2
 MOV AH, 9
 INT 21H
;GET SECOND NUM
 MOV AH, 1
 INT 21H
 MOV CL, AL

;PERFORM SUBTRACTION OPERATION
 SUB BL, CL
 ADD BL, 48
;DISPLAY ANSWER
 LEA DX, RESULT
 MOV AH, 9
 INT 21H

;GET ANSWER INTO DATA REG TO DISPLAY
 MOV AH, 2
 MOV DL, BL
 INT 21H
 MOV AH, 4CH
 INT 21H

MULTIPLICATION:
;PROMPT FIRST NUM
 LEA DX, PROMPT1
 MOV AH, 9
 INT  21H

;GET FIRST NUM INTO VAR 
 MOV AH, 1
 INT 21H

;CONVERT INTO NUM
 SUB AL, 30H
 MOV FNUM, AL

;PROMPT SECOND NUM
 LEA DX, PROMPT2
 MOV AH, 9
 INT 21H

;GET SECOND NUM INTO VAR
 MOV AH, 1
 INT 21H

;CONVERT SECOND NUM INTO NUMBER
 SUB AL, 30H
 MOV SNUM, AL

;PERFORM OPERATION IN AL REG 
 MUL FNUM

;PUT INTO RESU VARIABLE
 MOV RESU, AL
 AAM

 ADD AH, 30H
 ADD AL, 30H

 MOV BX, AX
;DISPLAY RESULT SENTENCE
 LEA DX, RESULT
 MOV AH, 9
 INT 21H

;PUT BOTH PARTS OF ANSWER INTO THE DL
 MOV AH, 2
 MOV DL, BH
 INT 21H

 MOV AH, 2
 MOV DL, BL
 INT 21H
 MOV AH, 4CH
 INT 21H

