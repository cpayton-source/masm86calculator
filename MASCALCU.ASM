 MOV FNUM, AL

;Prompt second num
 LEA DX, PROMPT2
 MOV AH, 9
 INT 21H

;Get second num and convert to number
 MOV AH, 1
 INT 21H
 SUB AL, 30H
 MOV SNUM, AL

;Perform necessary operations
 MOV CL, FNUM
 MOV CH, 00
 MOV AX, CX
