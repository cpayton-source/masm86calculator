;PROMPT FIRST NUM
 LEA DX, PROMPT1
 MOV AH, 9
 INT  21H

;GET FIRST NUM INTO VAR 
 MOV AH, 1
 INT 21H

;CONVERT INTO NUM
 SUB AL, 30H
 MOV FNUM, AL

;Perform Multiplications
 MUL FNUM
 MUL FNUM

;Convert Into numbers
 AAM

