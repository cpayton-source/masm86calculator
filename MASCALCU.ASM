MAIN PROC
  MOV AX, @DATA
  MOV DS, AX
  LEA DX, OP1
  MOV AH, 9
  INT 21H

  MOV AH, 1
  INT 21H
  MOV BH, AL
  SUB BH, 48

  CMP BH, 1
  JE ADDITION

  CMP BH, 2
  JNE GOD
  JMP SUBTRACTION
 GOD:
  CMP BH, 3
  JNE NEXT
  JMP MULTIPLICATION
 NEXT:
  CMP BH, 4
  JNE NEB
  JMP DIVISION
 NEB:
  CMP BH, 5
  JNE NONO
  JMP FACTORIAL
 NONO:
  CMP BH, 6
  JNE BEN
  JMP SQUARE
 BEN:
  CMP BH, 7
  JNE EXI
  JMP CUBE
 EXI:
  CMP BH, 8
  JNE EXIT
  JMP POWER
EXIT:
 MOV AH, 4CH
 INT 21H

